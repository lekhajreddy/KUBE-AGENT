"""
KubeMind — Production Validation Suite
Executes all 48 tasks from the production validation checklist.
"""
import httpx
import json
import subprocess
import sys
import time

BASE = "http://localhost:8000"
KUBECTL = "kubectl"

passed = 0
failed = 0
skipped = 0


def run(cmd, shell=True):
    try:
        r = subprocess.run(cmd, shell=True, capture_output=True, text=True, timeout=30)
        return r.stdout.strip(), r.returncode
    except subprocess.TimeoutExpired:
        return "", -1
    except Exception:
        return "", -1


def task(num, name, condition, detail=""):
    global passed, failed
    if condition:
        passed += 1
        print(f"  [PASS] Task {num}: {name}")
    else:
        failed += 1
        print(f"  [FAIL] Task {num}: {name}" + (f" — {detail}" if detail else ""))


def task_skip(num, name, reason="not applicable"):
    global skipped
    skipped += 1
    print(f"  [SKIP] Task {num}: {name} — {reason}")


def get(path):
    try:
        r = httpx.get(f"{BASE}{path}", timeout=15)
        return r.status_code, r.json() if r.text else {}
    except Exception as e:
        return 0, {"error": str(e)}


